int user_input()
{
     register int count=0;
    fd_set rfds;
    struct timeval tv;
    register int retval;
     int input = 0;
    tv.tv_sec = 3;
    tv.tv_usec = 0;
    FD_ZERO(&rfds);
    FD_SET(STDIN_FILENO, &rfds);
    retval = select(STDIN_FILENO + 1, &rfds, NULL, NULL, &tv);
    if (retval == -1)
    {
        perror("select()");
        exit(EXIT_FAILURE);
    } 
    else if (retval == 0) 
	    count++;
     else
	     scanf("%d", &input);
    
     system("clear");
    return input;
}
